// WAP that uses a switch case to determine the day of the week based on a number (1-7) and log the day name to the console 
let day="Monday"
switch(day){
    case "Monday":
        console.log("Monday");
        break;
    case "Tuesday":
        console.log("Tuesday");
        break;
    case "Wednesday":
        console.log("Wednesday");
        break;
    case "Thursday":
        console.log("Thursday");
        break;
    case "Friday":
        console.log("Friday");
        break;
    case "Saturday":
        console.log("Saturday");
        break;
    default:
        console.log("Sunday");
}

